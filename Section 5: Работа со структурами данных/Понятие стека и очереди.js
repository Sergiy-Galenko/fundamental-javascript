// FIFO, FILO

// Queue - first in, first out
// push/shift
const arr = [];
arr.push(4);
arr.push(7);
arr.push(9);
// console.log(arr); 
arr.shift();
// console.log(arr);

// Steck - first in, last out
// push/pop

const arr2 = [];
arr2.push(1);
arr2.push(2);
arr2.push(3);
arr2.push(5);
console.log(arr2);
arr2.pop();
arr2.pop();
console.log(arr2);
